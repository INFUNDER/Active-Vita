// Function to change the language
function changeLanguage(lang) {
    const data = languageData[lang];
    
    if (data) {
        document.querySelector('h1').textContent = data.title;
        document.querySelector('input[type="text"]').placeholder = data.placeholder;
        
        const labels = document.querySelectorAll('.radio-group label');
        labels.forEach((label, index) => {
            const input = label.querySelector('input');
            if (input) {
                label.textContent = data.labels[index];
                label.prepend(input);
            }
        });
        
        document.querySelector('.skip-button').textContent = data.skip;
        document.querySelector('button[type="button"]').textContent = data.next;
    }
}

// Event listener for language change
document.querySelectorAll('.radio-group input').forEach(input => {
    input.addEventListener('change', (event) => {
        const selectedLanguage = event.target.value;
        changeLanguage(selectedLanguage);
    });
});

// Page Navigation Functions
function nextStep() {
    const currentPage = getCurrentPage();
    const nextPage = getNextPage(currentPage);
    window.location.href = nextPage;
}

function getCurrentPage() {
    const currentPath = window.location.pathname;
    const currentPage = currentPath.substring(currentPath.lastIndexOf('/') + 1);
    return currentPage;
}

function getNextPage(currentPage) {
    switch (currentPage) {
        case 'index.html':
            return 'age.html';
        case 'age.html':
            return 'wherefrom.html';
        case 'wherefrom.html':
            return 'height.html';
        case 'height.html':
            return 'currentweight.html';
        case 'currentweight.html':
            return 'targetweight.html';
        case 'targetweight.html':
            return 'goal.html';
        case 'goal.html':
            return 'medical.html';
        default:
            return 'index.html';
    }
}

function goBack() {
    window.history.back();
}

function skip() {
    const currentPage = getCurrentPage();
    const nextPage = getNextPage(currentPage);
    window.location.href = nextPage;
}